package client;

public class SpecialCoreOption {
  private String condType;
  
  private int cooltime;
  
  private int count;
  
  private int validTime;
  
  private double prob;
  
  private String effectType;
  
  private int skillid;
  
  private int skilllevel;
  
  private int heal_percent;
  
  private int reducePercent;
  
  public String getCondType() {
    return this.condType;
  }
  
  public void setCondType(String condType) {
    this.condType = condType;
  }
  
  public int getCooltime() {
    return this.cooltime;
  }
  
  public void setCooltime(int cooltime) {
    this.cooltime = cooltime;
  }
  
  public int getCount() {
    return this.count;
  }
  
  public void setCount(int count) {
    this.count = count;
  }
  
  public int getValidTime() {
    return this.validTime;
  }
  
  public void setValidTime(int validTime) {
    this.validTime = validTime;
  }
  
  public double getProb() {
    return this.prob;
  }
  
  public void setProb(double prob) {
    this.prob = prob;
  }
  
  public String getEffectType() {
    return this.effectType;
  }
  
  public void setEffectType(String effectType) {
    this.effectType = effectType;
  }
  
  public int getSkillid() {
    return this.skillid;
  }
  
  public void setSkillid(int skillid) {
    this.skillid = skillid;
  }
  
  public int getSkilllevel() {
    return this.skilllevel;
  }
  
  public void setSkilllevel(int skilllevel) {
    this.skilllevel = skilllevel;
  }
  
  public int getHeal_percent() {
    return this.heal_percent;
  }
  
  public void setHeal_percent(int heal_percent) {
    this.heal_percent = heal_percent;
  }
  
  public int getReducePercent() {
    return this.reducePercent;
  }
  
  public void setReducePercent(int reducePercent) {
    this.reducePercent = reducePercent;
  }
}
